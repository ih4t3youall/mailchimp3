package ar.com.bit8.entities;

public class Bit8Response {

	private String draw_number;
	
	private String game_name;
	
	private String result_numbers;

	
	public String getDraw_number() {
		return draw_number;
	}

	public void setDraw_number(String draw_number) {
		this.draw_number = draw_number;
	}

	public String getGame_name() {
		return game_name;
	}

	public void setGame_name(String game_name) {
		this.game_name = game_name;
	}

	public String getResult_numbers() {
		return result_numbers;
	}

	public void setResult_numbers(String result_numbers) {
		this.result_numbers = result_numbers;
	}	
	
}
